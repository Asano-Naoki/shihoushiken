export function transformSubject(subject) {
    switch (subject) {
        case 'ken':
          return '憲法'
          break
        case 'gyo':
          return '行政法'
          break
        case 'min':
          return '民法'
          break
        case 'syo':
          return '商法'
          break
        case 'mso':
          return '民事訴訟法'
          break
        case 'kei':
          return '刑法'
          break
        case 'kso':
          return '刑事訴訟法'
          break
      }
}